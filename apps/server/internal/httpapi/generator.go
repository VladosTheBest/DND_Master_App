func shouldGenerateQuestIssuerDraft(input generateEntityDraftInput, entity createEntityInput) bool {
	if normalizeEntityKind(input.Kind) != "quest" {
		return false
	}
	if strings.TrimSpace(entity.IssuerID) != "" {
		return false
	}
	if input.Current != nil && strings.TrimSpace(input.Current.IssuerID) != "" {
		return false
	}
	return true
}

func buildQuestIssuerDraft(prompt string, quest createEntityInput) createEntityInput {
	issuerPrompt := buildQuestIssuerPrompt(prompt, quest)
	draft := buildNpcDraft(issuerPrompt)
	draft.LocationID = quest.LocationID
	draft.Role = firstNonEmpty(strings.TrimSpace(draft.Role), "Quest giver")
	draft.Summary = firstNonEmpty(strings.TrimSpace(draft.Summary), draft.Title+" is the NPC who sets the party on this quest.")
	draft.Content = firstNonEmpty(strings.TrimSpace(draft.Content), draft.Title+" knows the hidden stakes, real danger and what happens if the quest fails.")
	draft.PlayerContent = firstNonEmpty(strings.TrimSpace(draft.PlayerContent), draft.Title+" can explain the job, visible stakes, promised reward and only the information safe to reveal to the party.")
	return draft
}

func buildQuestIssuerPrompt(prompt string, quest createEntityInput) string {
	return strings.TrimSpace(firstNonEmpty(prompt, "Новый квест") + "\n\nCreate the quest giver NPC for this quest.\nQuest title: " + firstNonEmpty(quest.Title, "Новый квест") + "\nQuest summary: " + firstNonEmpty(quest.Summary, quest.Content) + "\nThe NPC must fit the same campaign tone, have clear motives, know more than the players, and have a separate player-facing version of the information.")
}

func draftTitle(prompt string, fallback string) string {
	trimmed := strings.TrimSpace(prompt)
	if trimmed == "" {
		return fallback
	}

	words := strings.Fields(trimmed)
	if len(words) > 5 {
		words = words[:5]
	}

	return strings.TrimSpace(strings.Join(words, " "))
}

func containsAny(value string, options ...string) bool {
	lower := strings.ToLower(value)
	for _, option := range options {
		if strings.Contains(lower, strings.ToLower(option)) {
			return true
		}
	}
	return false
}

func fallbackEncounterFollowerTitle(title string) string {
	trimmed := strings.TrimSpace(title)
	if trimmed == "" {
		return "Приспешник"
	}
	return trimmed + " отряд"
}

