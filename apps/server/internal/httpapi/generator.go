package httpapi

import "strings"

type AIOptions struct {
	Provider string
	Model    string
	BaseURL  string
	APIToken string
}

type entityGenerator interface {
	Generate(campaign campaignData, input generateEntityDraftInput) (generateEntityDraftResult, error)
	GenerateWorldEvent(campaign campaignData, input generateWorldEventInput) (generateWorldEventResult, error)
}

type encounterGenerator interface {
	GenerateEncounter(campaign campaignData, input generateCombatInput) (generateEncounterDraftResult, error)
}

type generateEncounterDraftResult struct {
	Provider string
	Notes    []string
	Items    []encounterDraftItem
}

type encounterDraftItem struct {
	Quantity int
	Entity   createEntityInput
}

type generatorConfig struct {
	requestedProvider string
	activeProvider    string
	model             string
	baseURL           string
	apiToken          string
	apiKeyConfigured  bool
}

type scaffoldGenerator struct {
	config generatorConfig
}

func newEntityGenerator(options AIOptions) entityGenerator {
	requestedProvider := normalizeProvider(options.Provider)
	baseURL := strings.TrimSpace(options.BaseURL)
	apiToken := strings.TrimSpace(options.APIToken)
	if requestedProvider == "" {
		if apiToken != "" || baseURL != "" {
			requestedProvider = "openai"
		} else {
			requestedProvider = "scaffold"
		}
	}

	config := generatorConfig{
		requestedProvider: requestedProvider,
		model:             strings.TrimSpace(options.Model),
		baseURL:           baseURL,
		apiToken:          apiToken,
		apiKeyConfigured:  apiToken != "",
	}

	switch requestedProvider {
	case "openai", "chatgpt":
		config.activeProvider = "openai"
		if config.model == "" {
			config.model = "gpt-5.4-mini"
		}
		return newOpenAIGenerator(config)
	default:
		config.activeProvider = "local-scaffold"
		if config.model == "" {
			config.model = "shadow-edge-synthetic-v1"
		}
		return scaffoldGenerator{config: config}
	}
}

func normalizeProvider(value string) string {
	return strings.ToLower(strings.TrimSpace(value))
}

func (generator scaffoldGenerator) Generate(_ campaignData, input generateEntityDraftInput) (generateEntityDraftResult, error) {
	kind := strings.TrimSpace(strings.ToLower(input.Kind))
	prompt := strings.TrimSpace(input.Prompt)

	result := generateEntityDraftResult{
		Provider: generator.config.activeProvider,
		Notes:    generator.buildNotes(),
	}

	switch kind {
	case "location":
		result.Entity = buildLocationDraft(prompt)
	case "npc":
		result.Entity = buildNpcDraft(prompt)
	case "monster":
		result.Entity = buildMonsterDraft(prompt)
	case "quest":
		result.Entity = buildQuestDraft(prompt)
	case "lore":
		result.Entity = buildLoreDraft(prompt)
	default:
		result.Entity = createEntityInput{
			Kind:    kind,
			Title:   draftTitle(prompt, "New Entity"),
			Summary: "Scaffold generation does not yet know how to structure this entity kind.",
			Content: prompt,
			Tags:    []string{"ai-draft"},
		}
	}

	if shouldGenerateQuestIssuerDraft(input, result.Entity) {
		result.LinkedDrafts = []linkedEntityDraft{
			{
				Role:   "issuer",
				Note:   "Новый квестодатель будет создан вместе с квестом, если ты не выберешь существующего НПС.",
				Entity: buildQuestIssuerDraft(prompt, result.Entity),
			},
		}
		result.Notes = append(result.Notes, "Так как квестодатель не выбран, AI подготовил отдельный draft НПС для этого квеста.")
	}

	return result, nil
}

func (generator scaffoldGenerator) GenerateWorldEvent(campaign campaignData, input generateWorldEventInput) (generateWorldEventResult, error) {
	return generateWorldEventResult{
		Provider: generator.config.activeProvider,
		Notes:    append(generator.buildNotes(), "Короткое событие собрано локальным scaffold-провайдером."),
		Event:    normalizeWorldEventDraftInput(campaign, input, buildWorldEventDraft(campaign, input)),
	}, nil
}

func (generator scaffoldGenerator) GenerateEncounter(_ campaignData, input generateCombatInput) (generateEncounterDraftResult, error) {
	notes := append([]string{}, generator.buildNotes()...)
	notes = append(notes, "Encounter roster generated by the local scaffold provider.")

	totalCount := normalizeEncounterMonsterCount(input.MonsterCount)
	prompt := strings.TrimSpace(input.Prompt)
	banditPrompt := containsAny(prompt, "bandit", "разбой", "brigand", "raider")
	leaderPrompt := containsAny(prompt, "leader", "глав", "captain", "chief", "boss")

	if banditPrompt {
		items := make([]encounterDraftItem, 0, 2)
		if leaderPrompt {
			items = append(items, encounterDraftItem{
				Quantity: 1,
				Entity:   buildOfficialBanditCaptainDraft(),
			})
		}

		minionCount := totalCount
		if leaderPrompt && minionCount > 1 {
			minionCount--
		}
		if minionCount > 0 {
			items = append(items, encounterDraftItem{
				Quantity: minionCount,
				Entity:   buildOfficialBanditDraft(),
			})
		}

		return generateEncounterDraftResult{
			Provider: generator.config.activeProvider,
			Notes:    notes,
			Items:    items,
		}, nil
	}

	if leaderPrompt && totalCount > 1 {
		leader := buildMonsterDraft(prompt)
		leader.Title = draftTitle(prompt, "Главарь отряда")
		leader.Importance = "Boss"
		minion := buildMonsterDraft(prompt)
		minion.Title = fallbackEncounterFollowerTitle(minion.Title)
		minion.Importance = "Standard"

		return generateEncounterDraftResult{
			Provider: generator.config.activeProvider,
			Notes:    notes,
			Items: []encounterDraftItem{
				{Quantity: 1, Entity: leader},
				{Quantity: totalCount - 1, Entity: minion},
			},
		}, nil
	}

	switch {
	case containsAny(prompt, "bandit", "разбой", "brigand", "raider"):
		leaderRequested := containsAny(prompt, "leader", "глав", "captain", "chief", "boss")
		items := make([]encounterDraftItem, 0, 2)
		if leaderRequested {
			items = append(items, encounterDraftItem{
				Quantity: 1,
				Entity:   buildOfficialBanditCaptainDraft(),
			})
		}

		minionCount := totalCount
		if leaderRequested && minionCount > 1 {
			minionCount--
		}
		if minionCount > 0 {
			items = append(items, encounterDraftItem{
				Quantity: minionCount,
				Entity:   buildOfficialBanditDraft(),
			})
		}

		return generateEncounterDraftResult{
			Provider: generator.config.activeProvider,
			Notes:    notes,
			Items:    items,
		}, nil
	case containsAny(prompt, "leader", "глав", "captain", "chief", "boss") && totalCount > 1:
		leader := buildMonsterDraft(prompt)
		leader.Title = draftTitle(prompt, "Главарь отряда")
		leader.Importance = "Boss"
		minion := buildMonsterDraft(prompt)
		minion.Title = fallbackEncounterFollowerTitle(minion.Title)
		minion.Importance = "Standard"

		return generateEncounterDraftResult{
			Provider: generator.config.activeProvider,
			Notes:    notes,
			Items: []encounterDraftItem{
				{Quantity: 1, Entity: leader},
				{Quantity: totalCount - 1, Entity: minion},
			},
		}, nil
	default:
		return generateEncounterDraftResult{
			Provider: generator.config.activeProvider,
			Notes:    notes,
			Items: []encounterDraftItem{
				{
					Quantity: totalCount,
					Entity:   buildMonsterDraft(prompt),
				},
			},
		}, nil
	}
}

func (generator scaffoldGenerator) buildNotes() []string {
	notes := []string{
		"Draft generated by the local scaffold provider.",
		"Review links, numbers and world tone before using it in play.",
	}

	if generator.config.requestedProvider != "" && generator.config.requestedProvider != "scaffold" {
		notes = append(notes, "Requested provider "+generator.config.requestedProvider+" is not available, so the server fell back to local-scaffold.")
	}

	if generator.config.model != "" {
		notes = append(notes, "Configured model: "+generator.config.model+".")
	}

	if generator.config.baseURL != "" {
		notes = append(notes, "A provider base URL is already configured on the server.")
	}

	if generator.config.apiKeyConfigured {
		notes = append(notes, "An API key is already available on the server.")
	}

	return notes
}

func buildLocationDraft(prompt string) createEntityInput {
	category := "City"
	danger := "Tense"
	title := draftTitle(prompt, "New City")

	switch {
	case containsAny(prompt, "ruin", "ancient", "vault", "catacomb"):
		category = "Dungeon"
		danger = "Deadly"
	case containsAny(prompt, "region", "valley", "forest", "coast", "frontier"):
		category = "Region"
		danger = "Dangerous"
	case containsAny(prompt, "bridge", "temple", "tower", "gate", "shrine"):
		category = "POI"
	}

	return createEntityInput{
		Kind:     "location",
		Title:    title,
		Subtitle: category + " - AI draft",
		Summary:  "Location generated from a text prompt and ready for GM polish.",
		Content:  title + " is sketched as a usable scene hub with hooks, rumors and pressure points for the table.",
		Tags:     []string{"ai-draft", strings.ToLower(category)},
		Category: category,
		Region:   "New Region",
		Danger:   danger,
		QuickFacts: []quickFact{
			{Label: "Danger", Value: danger, Tone: "warning"},
			{Label: "Category", Value: category},
			{Label: "Source", Value: "AI draft"},
		},
	}
}

